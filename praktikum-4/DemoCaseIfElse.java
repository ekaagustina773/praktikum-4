public class DemoCaseIfElse {

    public static void main(String[] args) {
        int stok = 47;
        int jumlahbilangan = 54;

        if(jumlahbilangan == stok){
            System.out.println("Stok barang tidak mencukupi");
        }else{
            System.out.println("Data sudah sesuai");
        }
    }

}